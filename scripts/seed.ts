interface SeedPost {
  type: string;
  title: string;
  body: string;
  author: string;
  tags: string[];
  color: string;
  mediaURL: string | null;
  createdAt: Timestamp;
}

function buildPosts(): SeedPost[] {
  const posts: SeedPost[] = [];
  let hour = 10;

  function addQuote(q: Q) {
    hour += Math.floor(Math.random() * 8) + 2;
    posts.push({ type: "quote", title: q[0], body: q[1], author: q[2], tags: q[3], color: q[4], mediaURL: pickImage(q[4]), createdAt: hoursAgo(hour) });
  }

  function addNote(n: N) {
    hour += Math.floor(Math.random() * 8) + 2;
    posts.push({ type: "note", title: n[0], body: n[1], author: n[2], tags: n[3], color: n[4], mediaURL: pickImage(n[4]), createdAt: hoursAgo(hour) });
  }

  // Add all content
  JEWISH_QUOTES.forEach(addQuote);
  MORE_JEWISH.forEach(addQuote);
  STOIC_QUOTES.forEach(addQuote);
  SUFI_QUOTES.forEach(addQuote);
  BUDDHIST_QUOTES.forEach(addQuote);
  EASTERN_QUOTES.forEach(addQuote);
  MODERN_QUOTES.forEach(addQuote);
  MORE_WISDOM.forEach(addQuote);
  PRACTICE_NOTES.forEach(addNote);
  MORE_PRACTICES.forEach(addNote);
  NATURE_QUOTES.forEach(addQuote);
  EXTRA_QUOTES.forEach(addQuote);

  // Shuffle for variety
  for (let i = posts.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [posts[i], posts[j]] = [posts[j], posts[i]];
  }

  return posts;
}

